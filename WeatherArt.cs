    public static string GetWeatherArt(string weatherType)
    {
        var normalizedType = weatherType?.ToLower() ?? "calm";

        if (_weatherArt.TryGetValue(normalizedType, out var art))
        {
            return art;
        }

        return _weatherArt["calm"];
    }

    public static string GetColorForWeatherType(string weatherType)
    {
        return weatherType?.ToLower() switch
        {
            "calm" => "cyan",
            "rain" => "blue",
            "stormy" => "red",
            "sleet" => "red",
            "sunny" => "yellow",
            "cloudy" => "grey",
            "snow" => "white",
            "windy" => "aqua",
            "fog" => "grey",
            _ => "cyan"
        };
    }
